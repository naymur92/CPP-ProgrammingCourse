#include <iostream>

using namespace std;

int main(){
    int num1 = 15;          // Decimal
    int num2 = 017;         // Octal
    int num3 = 0x0f;        // Hexadecimal
    int num4 = 0b00001111;  // Binary

    cout << "Number 1 = " << num1 << "\n";
    cout << "Number 2 = " << num2 << "\n";
    cout << "Number 3 = " << num3 << "\n";
    cout << "Number 4 = " << num4 << "\n";
}